        <h2>Biography</h2>
        <p>
          Substantially the circumstances of her martyrdom are believed to be
          authentic, though the legend cannot
          <br />be proven true, and many details of the fifth century Acts of
          Saint Agnes are open to criticism. A church <br />was built over her
          tomb, and her relics venerated. According to tradition, Agnes was a
          member of the Roman <br />nobility, born in AD 291 and raised in an
          early Christian family. She suffered martyrdom at the age of
          <br />twelve or thirteen during the reign of the Roman emperor
          Diocletian, on 21 January 304. A beautiful young <br />girl from a
          wealthy family, Agnes had many suitors of high rank, and the young men,
